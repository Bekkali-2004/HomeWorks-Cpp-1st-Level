#include <iostream>

using namespace std;

int main (){

    int a = 10 , b = 20;

    a += b; // a + b
    cout << a << endl;
    a -= b; // a - b
    cout << a << endl;
    a *= b; // a * b
    cout << a << endl;
    a /= b; // a / b
    cout << a << endl;
    a %= b; // a % b
    cout << a << endl;

    return 0;
}