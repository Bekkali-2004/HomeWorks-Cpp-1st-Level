#include <iostream>

using namespace std;

int main (){

    cout << "For Loops : " << endl;
    for (int x = 0; x <= 5; x++)
    {
        cout << x << endl;
    }
    
    cout << endl;
    cout << "**************************" << endl << endl;

    cout << "While Loops : " << endl;

    int x = 0;
    while (x <= 5)
    {
        cout << x << endl;
        x++;
    }
    
    
    return 0;
}