#include <iostream>

using namespace std;

int main (){

    int A, B;

    cout << "Enter The 1st Number A : " << endl;
    cin >> A;
    cout << "Enter The 2nd Number B : " << endl;
    cin >> B;

    cout << "-----------------------------------------" << endl;

    cout << (A == B) << endl;
    cout << (A != B) << endl;
    cout << (A >= B) << endl;
    cout << (A <= B) << endl;
    cout << (A > B) << endl;
    cout << (A < B) << endl;

    return 0;
}